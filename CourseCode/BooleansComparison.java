public class BooleansComparison {
    public static void main(String[] args) {
        int chemistryGrade = 95;
        int biologyGrade = 75;

        System.out.println(biologyGrade > chemistryGrade);

        double sales = 36.55;
        double costs = 5.55;

        System.out.println(sales > costs);
    }
}
